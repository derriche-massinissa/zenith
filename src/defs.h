/*
 * Component masks, used to know what component a given GameObject
 * inherited from (Like needing to know if the Object has a Size to
 * determine if it can be rendered).
 */
#define COMPONENT_MASK_ALPHA			0b0000000000000000000000000000001
#define COMPONENT_MASK_BLENDMODE		0b0000000000000000000000000000010
#define COMPONENT_MASK_BOUNDS			0b0000000000000000000000000000100
#define COMPONENT_MASK_CROP				0b0000000000000000000000000001000
#define COMPONENT_MASK_DEPTH			0b0000000000000000000000000010000
#define COMPONENT_MASK_FLIP				0b0000000000000000000000000100000
#define COMPONENT_MASK_MASK				0b0000000000000000000000001000000
#define COMPONENT_MASK_ORIGIN			0b0000000000000000000000010000000
#define COMPONENT_MASK_PATHFOLLOWER		0b0000000000000000000000100000000
#define COMPONENT_MASK_SCROLLFACTOR		0b0000000000000000000001000000000
#define COMPONENT_MASK_SIZE				0b0000000000000000000010000000000
#define COMPONENT_MASK_TEXTURE			0b0000000000000000000100000000000
#define COMPONENT_MASK_TEXTURECROP		0b0000000000000000001000000000000
#define COMPONENT_MASK_TINT				0b0000000000000000010000000000000
#define COMPONENT_MASK_TRANSFORM		0b0000000000000000100000000000000
#define COMPONENT_MASK_VISIBLE			0b0000000000000001000000000000000

#endif
